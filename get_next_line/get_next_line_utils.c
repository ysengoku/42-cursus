char	*ft_strchr(const char *s, int c)
{
	while (s)
	{
		if (s == (char)c)
			return (s);
		s++;
	}
	if (s == (char)c)
		return (s);
	return (NULL);
}

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

